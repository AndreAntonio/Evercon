                            O acidente começa a ser evitado quando o veículo é bem segurado.<br><br>
 
O seguro Automóvel visa cobrir os prejuízos causados a seu veículo, que podem ser ocasionados por colisão, incêndio e roubo. Quando contratada a cobertura para terceiros (RCF), ele também indeniza os danos materiais e corporais em possíveis sinistros originados pelo segurado.
 <br>
Na Evercon Consultoria, você encontra soluções personalizadas e atendimento especializado para seu seguro Auto, com as coberturas que você precisa. Por isso, é importante que seu perfil seja preenchido com todo cuidado e atenção e que as informações prestadas e características do bem/veículo estejam corretas, de forma a não comprometer um possível sinistro e sua indenização.
 <br>
Além disso, a Evercon Consultoria trabalha em parceria com as melhores seguradoras do setor, com o objetivo de oferecer proteção e tranquilidade, a cada um de seus clientes. Nosso papel é analisar os riscos aos quais o segurado está exposto e buscar o produto mais adequado às suas necessidades, considerando sempre o melhor custo x benefício.
<br>
Abaixo, algumas dicas importantes para você levar em conta no momento de contratação da sua apólice:
 <br>
Não se esqueça de contratar uma cobertura de Responsabilidade Civil Facultativa (RCF): Ela visa garantir a indenização de valores por danos materiais, pessoais e/ou morais causados a terceiros, sendo todos eles involuntários, ocasionados por sinistros dos quais o segurado seja comprovadamente responsável. Além de evitar transtornos em possíveis acidentes envolvendo terceiros, as coberturas de RCF asseguram a tranquilidade do cliente na regulação do sinistro, ao não exigir pagamento de franquia; É importante atentar-se ao preenchimento correto do valor de cada cobertura, considerando as suas necessidades e a eventualidade da ocorrência de situações indesejadas;
 <br>
Não deixe de consultar as coberturas acessórias, que visam promover mais comodidade e segurança, como Carro Reserva, Limites de Kilometragem para uso do serviço de Guincho e Despesas Extraordinárias.
 v
Coberturas
 <br>
Básicas
 <br>
São duas as opções de coberturas básicas:
 <br>
Cobertura Compreensiva (Colisão, Incêndio, Roubo/Furto);
 <br>
Cobertura Incêndio e Roubo/Furto;
 <br>
Coberturas adicionais.
 <br>
Por uma pequena diferença no valor da sua apólice, você pode acrescentar as seguintes coberturas em seu Seguro Auto:
 <br>
Acessórios: garante a indenização por perdas para acessórios (equipamentos de som e afins), equipamentos, carrocerias e similares, desde que tais bens estejam discriminados na apólice.**
 <br>
Danos a vidros, lanternas, faróis e retrovisores do veículo: em caso de quebra ou trinca nos vidros (pára-brisa, laterais e traseiro) ou retrovisores externos (lente/espelho e carcaça), lanternas e faróis do seu veículo, você tem direito a troca ou reparo dos mesmos.
 <br>
Carro Reserva: garante a locação de um veículo extra, por um prazo mínimo de 7 dias, para utilização em caso de um sinistro com seu automóvel, em evento coberto pelo seguro.
 <br>
Acidentes Pessoais de Passageiros (APP): garante indenização ao motorista e passageiros do veículo – ou a seus beneficiários legais – em caso de acidente que os atinja e que resulte em morte ou invalidez permanente. Despesas Extraordinárias: reembolsa as despesas que você possa vir a ter, até o limite máximo de indenização, quando ficar sem o seu veículo por indenização integral.
 <br>
**Demais acessórios estão incluídos no valor destinado ao veículo (casco) não se aplicando a eles a exigência acima. Eles terão, porém, a cobrança da mesma franquia fixada ao veículo.
 <br>
Para O Veículo Do Terceiro
 <br>
Responsabilidade Civil Facultativa (RCF): garante indenização dos valores que o cliente tenha de pagar a terceiros por danos materiais e/ou pessoais e/ou morais, todos involuntários, causados por sinistros dos quais ele seja comprovadamente responsável. Não há franquia para as coberturas de RCF.
